    <script>
        document.addEventListener("DOMContentLoaded", () => {
            const milestones = document.querySelectorAll('.milestone');

            const checkVisibility = () => {
                const triggerBottom = window.innerHeight / 5 * 4;

                milestones.forEach(milestone => {
                    const milestoneTop = milestone.getBoundingClientRect().top;

                    if (milestoneTop < triggerBottom) {
                        milestone.classList.add('visible');
                    } else {
                        milestone.classList.remove('visible');
                    }
                });
            };

            checkVisibility();
            window.addEventListener('scroll', checkVisibility);
        });
    </script>

    <script>
        // Get all tab links
        let tabs = document.querySelectorAll('.tab-link');

        // Add click event to each tab link
        tabs.forEach(tab => {
            tab.addEventListener('click', function() {
                // Remove active class from all tabs
                tabs.forEach(item => item.classList.remove('active'));

                // Add active class to clicked tab
                this.classList.add('active');

                // Get all tab content containers
                let contents = document.querySelectorAll('.tab-content');

                // Hide all tab content
                contents.forEach(content => content.classList.remove('active'));

                // Show the content corresponding to the clicked tab
                let tabId = this.getAttribute('data-tab');
                document.getElementById(tabId).classList.add('active');
            });
        });
    </script>

    <script>
        const text = "74 400 75 400";
        let index = 0;
        const speed = 100; // Typing speed in milliseconds

        function typeWriter() {
            if (index < text.length) {
                document.getElementById("typewriter-text").innerHTML += text.charAt(index);
                index++;
                setTimeout(typeWriter, speed);
            } else {
                // Reset to create a continuous effect
                setTimeout(() => {
                    document.getElementById("typewriter-text").innerHTML = '';
                    index = 0;
                    typeWriter(); // Start typing again
                }, 2000); // Delay before resetting
            }
        }

        window.onload = typeWriter;
    </script>




